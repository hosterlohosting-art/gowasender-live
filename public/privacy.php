PRIVACY POLICY FOR HOSTERLO LLC Last Updated: January 3, 2026

1. Introduction Hosterlo LLC ("we," "us," or "our") prioritizes the privacy of our users. This policy outlines how we
handle data when you use our WhatsApp Cloud API services.

2. Data We Collect We only collect essential information required to provide our communication services, specifically:

Phone Numbers: To send and receive WhatsApp messages.

Message Content: We process message data to transmit it between you and your recipients. Messages are encrypted during
transmission.

3. How We Use Your Data

To operate and maintain our WhatsApp messaging services.

To communicate with you regarding service updates or customer support.

We do not sell your personal data to third parties.

4. Data Security & Meta Integration Our services use the WhatsApp Cloud API provided by Meta. All communications are
protected by end-to-end encryption using the Signal protocol. Meta acts as a data processor on our behalf.

5. Data Retention We retain phone numbers only as long as necessary to provide our services. Message data is typically
deleted from Meta's servers after 30 days.

6. User Rights You have the right to access, correct, or request the deletion of your personal data at any time by
contacting us at: thedigioverse@gmail.com.